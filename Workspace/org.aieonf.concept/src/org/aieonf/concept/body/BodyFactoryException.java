package org.aieonf.concept.body;

public class BodyFactoryException extends Exception {

	/**
	 * for serialization purposes
	*/
	private static final long serialVersionUID = -8084526860532329689L;

	public BodyFactoryException() {
		super();
	}

	public BodyFactoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public BodyFactoryException(String message) {
		super(message);
	}

	public BodyFactoryException(Throwable cause) {
		super(cause);
	}

}
